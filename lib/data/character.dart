class Character {
  final int id;
  final String name;
  final String birthday;
  final List occupation;
  final String img;
  final String status;
  final String nickname;
  final List appearance;
  final String portrayed;
  final dynamic category;

  const Character(
      {this.id,
      this.name,
      this.birthday,
      this.occupation,
      this.img,
      this.status,
      this.nickname,
      this.appearance,
      this.portrayed,
      this.category});
}
